         $request->validate([
            'name' => 'required',
            'price' => 'required',
        ]);

        if (Service::where('name', '=', Input::get('name'))->exists()) {
            return redirect()->back()->with('error', 'Service Already Exists');
        }else{
            $service = Service::create([
                'name' => $request->name,
                'price' => $request->price
            ]);

            return redirect()->back()->with('success', 'Service Saved Successfully');
        }
    }

